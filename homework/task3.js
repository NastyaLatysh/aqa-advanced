let a = 7;

let i = 1;
while (i <= 10){
    console.log(`${a} * ${i} = ${a * i}`);
    i++;
}



for (let y = 1 ; y <= 10 ; y++){
    console.log(`${a} * ${y} = ${a * y}`);
}